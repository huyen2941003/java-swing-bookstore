    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        // TODO add your handling code here:
        conn = db.getConnection();
        try {
            DefaultTableModel model = (DefaultTableModel)table_NXB.getModel();
            model.setRowCount(0);
                
            if(txt_timkiem.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Vui Lòng nhập thông tin cần tìm kiếm");
                HienTHi();
            } else{
                String sql = "select * from NhaXuatBan where TenNhaXuatBan like N'%" + txt_timkiem.getText() + "%'";
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                
                while(rs.next()){
                    Object[] row = new Object[4];
                    row[0] = rs.getString("MaNhaXuatBan");
                    row[1] = rs.getString("TenNhaXuatBan");
                    row[2] = rs.getString("DiaChi");
                    row[3] = rs.getString("DienThoai");
                    
                    model.addRow(row);
                    
                }
                if(model.getRowCount() > 0)
                {
                    JOptionPane.showMessageDialog(this, "Có " + model.getRowCount() + " sách mà bạn muốn tìm!");
                    loadLai();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả");
                }
                rs.close();
                st.close();
                conn.close();
            }
        } catch (Exception e) {
            e.toString();
        }
    }//GEN-LAST:event_btn_timkiemActionPerformed

