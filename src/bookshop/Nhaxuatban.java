    private void btn_ThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ThemActionPerformed
        // TODO add your handling code here:
        conn = db.getConnection();
        try {
            if(txt_MaNXB.getText().equals("")||txt_TenNXB.getText().equals("")||
                    txt_DiaChi.getText().equals("")||txt_SDT.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Bạn cần nhập đủ dữ liệu");
            }else{
                StringBuffer sb = new StringBuffer();
                String querry = "select MaNhaXuatBan from NhaXuatBan where MaNhaXuatBan = '" + txt_MaNXB.getText()+"'";
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(querry);
                if(rs.next()){
                    sb.append("Mã nhà xuất bản này đã tồn tại");
                }
                if(sb.length() > 0){//nếu if kia đúng thig nó sẽ thêm vào sb 1 đoạn string,ktra độ dài chuỗi này nếu lớn hơn 0 tức là có thông báo
                    JOptionPane.showMessageDialog(this, sb.toString());
                }else{ 
                    String sql = "insert into NhaXuatBan values('"+txt_MaNXB.getText()+"',N'"+txt_TenNXB.getText()+"',N'"+txt_DiaChi.getText()+"',"
                            + "'"+txt_SDT.getText()+"')";
                    st = conn.createStatement();
                    int kq = st.executeUpdate(sql);
                    if(kq>0){
                        JOptionPane.showMessageDialog(this, "Thêm thành công");
                        loadLai();
                        HienTHitb2();
                    }else{
                        JOptionPane.showMessageDialog(this, "Thêm thất bại");                        
                    }
                    st.close();
                }
                conn.close();
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi nhập liệu: " + e.getMessage(), "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_ThemActionPerformed

