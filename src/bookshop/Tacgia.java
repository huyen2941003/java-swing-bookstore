    private void btn_XoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_XoaActionPerformed
        // TODO add your handling code here:
        conn = db.getConnection();
        try {
            String matg = txt_matg.getText();
            if (matg == null || matg.trim().isEmpty()) 
            {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã tác giả trước khi xóa");
            return;
            }
            else
            {
                Statement st = conn.createStatement();
                String sql = "delete from TacGia where matacgia = '"+matg+"'";
                st = conn.createStatement();
                int t = JOptionPane.showConfirmDialog(this,"Bạn có muốn xóa?","Thông báo!", JOptionPane.YES_NO_OPTION);
                if(t == JOptionPane.YES_OPTION){
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    loadlai();
                    Hienthi2();
                }else{
                    JOptionPane.showMessageDialog(this, "Xóa thất bại");                        
                }
                st.close();
                conn.close();
            }
        }
        catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Mã Tác giả " + txt_matg.getText().toString() + " đang liên kết với Bảng Sách nên bạn không thể xóa được");
        }
    }//GEN-LAST:event_btn_XoaActionPerformed

