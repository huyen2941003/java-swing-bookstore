    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        // TODO add your handling code here:
        conn = db.getConnection();
        try {
            DefaultTableModel model = (DefaultTableModel)table_tacgia.getModel();
            model.setRowCount(0);
                
            if(txt_timkiem.getText().equals(""))
            {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập thông tin cần tìm");
                Hienthi();
            } else{
                String sql = "select * from TacGia where TenTongChuBien like N'%" + txt_timkiem.getText() + "%'" + "or TenPhoChuBien like N'%" + txt_timkiem.getText() + "%'";
                Statement st = conn.createStatement();
                ResultSet rs = st.executeQuery(sql);
                
                while(rs.next()){
                    Object[] row = new Object[4];
                    row[0] = rs.getString("MaTacGia");
                    row[1] = rs.getString("TenTongChuBien");
                    row[2] = rs.getString("TenPhoChuBien");
                    row[3] = rs.getString("LienHe");
                    
                    model.addRow(row);
                    
                }
                if(model.getRowCount() > 0)
                {
                    JOptionPane.showMessageDialog(this, "Có " + model.getRowCount() + " tác giả mà bạn muốn tìm!");
                    loadlai();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả");
                }
                rs.close();
                st.close();
                conn.close();
            }
        } catch (Exception e) {
            e.toString();
        }
    }//GEN-LAST:event_btn_timkiemActionPerformed

